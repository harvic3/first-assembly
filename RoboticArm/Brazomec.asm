;RETARDOS DE 50 Y 20 ms Y ANIDADOS DE 100 ms Y 40 ms

RETARDO50m 		movlw     .55       ; 1 set number of repetitions (B)
        		movwf     PDel0     ; 1 |
PLoop10  		movlw     .181      ; 1 set number of repetitions (A)
        		movwf     PDel1     ; 1 |
PLoop20  		clrwdt              ; 1 clear watchdog
        		clrwdt              ; 1 cycle delay
        		decfsz    PDel1, 1  ; 1 + (1) is the time over? (A)
        		goto      PLoop20    ; 2 no, loop
        		decfsz    PDel0,  1 ; 1 + (1) is the time over? (B)
        		goto      PLoop10    ; 2 no, loop
        		return              ; 2+2 Done


RETARDO20m    	movlw     .21       ; 1 set number of repetitions (B)
        		movwf     PD0     ; 1 |
PLoop1 			movlw     .237      ; 1 set number of repetitions (A)
        		movwf     PD1     ; 1 |
PLoop2  		clrwdt              ; 1 clear watchdog
        		decfsz    PD1, 1  ; 1 + (1) is the time over? (A)
        		goto      PLoop2    ; 2 no, loop
        		decfsz    PD0,  1 ; 1 + (1) is the time over? (B)
        		goto      PLoop1    ; 2 no, loop
PDelL1  		goto 	  PDelL2         ; 2 cycles delay
PDelL2  		clrwdt              ; 1 cycle delay
        		return              ; 2+2 Done


RETARX4		MOVLW	.5
			MOVWF	REG1
IN1			CALL	RETARDO20m
			DECFSZ	REG1
			GOTO	IN1
			RETURN

